#include "libft.h"

int	ft_atoi(char *str)
{
	int	res;
	int	negative;
	int	flag;

	flag = 0;
	negative = 1;
	res = 0;
	while (*str && (*str == ' ' || *str == '\n' || *str == '\t'
			|| *str == '\v' || *str == '\f' || *str == '\r'))
		str++;
	while (*str && (*str == '-' || *str == '+'))
	{
		if (*str == '-')
			negative *= -1;
		if (flag == 1)
			return (0);
		str++;
		flag = 1;
	}
	while (*str && *str >= '0' && *str <= '9')
	{
		res = res * 10 + (*str - 48);
		str++;
	}
	return (res * negative);
}
